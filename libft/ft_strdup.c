#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		size;
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	size = ft_strlen(s);
	dup = malloc(sizeof(char) * (size) + 1);
	if (!dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
